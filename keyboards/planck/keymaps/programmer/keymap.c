void persistent_default_layer_set(uint16_t default_layer) {
  eeconfig_update_default_layer(default_layer);
  default_layer_set(default_layer);
}

#define COMMAND_ALT(kc) MACRO(D(LGUI), D(LALT), T(kc), U(LALT), U(LGUI), END)
#define COMMAND_CTRL(kc) MACRO(D(LGUI), D(LCTL), T(kc), U(LCTL), U(LGUI), END)
#define COMMAND_SHIFT_CTRL(kc) MACRO(D(LGUI), D(LCTL), D(LSHIFT), T(kc), U(LSHIFT), U(LCTL), U(LGUI), END)

const macro_t *action_get_macro(keyrecord_t *record, uint8_t id, uint8_t opt) {
    if (record->event.pressed) {
        switch(id) {
            case 0: // Center Window
                return COMMAND_ALT(C);
            case 1: // Top
                return COMMAND_ALT(UP);
            case 2: // Bottom
                return COMMAND_ALT(DOWN);
            case 3: // Left
                return COMMAND_ALT(LEFT);
            case 4: // Right
                return COMMAND_ALT(RIGHT);
            case 5: // Up Left
                return COMMAND_CTRL(LEFT);
            case 6: // Down left
                return COMMAND_SHIFT_CTRL(LEFT);
            case 7: // Up right
                return COMMAND_CTRL(RIGHT);
            case 8: // Down right
                return COMMAND_SHIFT_CTRL(RIGHT);
            case 9: // Fullscreen
                return COMMAND_SHIFT_CTRL(F); 
        }
    }
    return MACRO_NONE;
};

bool process_record_user(uint16_t keycode, keyrecord_t *record) {
  switch (keycode) {
    case QWERTY:
      if (record->event.pressed) {
        #ifdef AUDIO_ENABLE
          PLAY_NOTE_ARRAY(tone_qwerty, false, 0);
        #endif
        persistent_default_layer_set(1UL<<_QWERTY);
      }
      return false;
      break;
    case LOWER:
      if (record->event.pressed) {
        layer_on(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
        layer_off(_LOWER);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      }
      return false;
      break;
    case RAISE:
      if (record->event.pressed) {
        layer_on(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      } else {
        layer_off(_RAISE);
        update_tri_layer(_LOWER, _RAISE, _ADJUST);
      }
      return false;
      break;
    case SONG:
      #ifdef AUDIO_ENABLE
        PLAY_NOTE_ARRAY(wii_sports, false, 0);
      #endif
      return false;
      break;
  }
  return true;
}

